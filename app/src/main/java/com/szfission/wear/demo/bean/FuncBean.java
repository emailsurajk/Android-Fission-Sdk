package com.szfission.wear.demo.bean;

public class FuncBean {
    private int title;
    private int func;

    public FuncBean(int title, int func) {
        this.title = title;
        this.func = func;
    }

    public int getTitle() {
        return title;
    }

    public void setTitle(int title) {
        this.title = title;
    }

    public int getFunc() {
        return func;
    }

    public void setFunc(int func) {
        this.func = func;
    }
}
